package day16;

public class LOOP {
    public static void main(String[] args) {

        int bananas = 1;
        while (bananas<=10){
            System.out.println("EATING BANANA" + bananas);
            bananas ++ ;
            System.out.println("count of banana is = " + bananas);
        } System.out.println("NO MORE BANANAS");

        int x = 0 ;
        while (x<=5) {
            System.out.println("MERRY CHRISTMAS");
            x ++ ;
        }


        int count = 1 ;
        while (count<=5) {
            System.out.println("Hello World");
            count++;
        }
    }
}
