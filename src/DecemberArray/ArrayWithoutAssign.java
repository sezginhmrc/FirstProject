package DecemberArray;

public class ArrayWithoutAssign {
    public static void main(String[] args) {

        // Array without assgin value ;

        int [] number = new int[4] ;
        System.out.println(number[3]);

        char [] name = new char[3];
        System.out.println(name[0]);

        boolean [] marriedOrNot = new boolean[4] ;
        System.out.println(marriedOrNot[2]);

        String [] lovedOnes = new String[4] ;
        System.out.println(lovedOnes[3]);


    }
}
