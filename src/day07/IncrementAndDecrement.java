package day07;

public class IncrementAndDecrement {
    public static void main(String[] args) {

        int apple = 10 ;

        apple = apple + 1 ; // + 1
        apple +=1 ;         // + 1
        ++ apple ;          // + 1
        apple ++ ;          // + 1

        System.out.println(apple);

        -- apple ;
        apple -=1 ;
        apple = apple -1 ;
        apple -- ;

        System.out.println( apple );


    }
}

